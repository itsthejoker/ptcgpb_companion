class Dex:
    cards = _cards

    def __getitem__(self, item):
        return self.get_by_id(item)

    def get_by_id(self, card_id: str):
        if not isinstance(card_id, str):
            raise TypeError("Card ID must be a string")

        pieces = card_id.split("_")
        if len(pieces) < 2:
            raise TypeError("Card ID must be in the format of {set_id}_{number}")

        if len(pieces) == 2:
            set_id, number_str = pieces
        else:
            set_id, number_str = pieces[-2:]
        
        number = int(number_str)

        return next(
            filter(
                lambda card: card.set_id == set_id and card.number == int(number),
                self.cards,
            ),
            None,
        )

    def get_by_set(self, set_id: str):
        return list(filter(lambda card: card.set_id == set_id, self.cards))

    def get_by_name(self, name: str):
        return list(filter(lambda card: name in card.name, self.cards))

    def get(self, card_code, default):
        return self.get_by_id(card_code) or default

    def items(self):
        return [(card.id, card.name, card) for card in self.cards]
